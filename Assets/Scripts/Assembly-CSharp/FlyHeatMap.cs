public class FlyHeatMap
{
	public float percent;

	public int i;

	public int j;

	public float xPos;

	public float zPos;

	public float distanceFromDragon;

	public FlyHeatMap(float percent, int i, int j, float xPos, float zPos)
	{
		this.percent = percent;
		this.i = i;
		this.j = j;
		this.xPos = xPos;
		this.zPos = zPos;
	}
}
